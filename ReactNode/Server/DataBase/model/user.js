import { mongoose } from "mongoose";

const studentSchema = new mongoose.Schema({
  name: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
  },
  password: {
    type: String,
    require: true,
  },
});

const user = new mongoose.model("user", studentSchema);

export default user;
